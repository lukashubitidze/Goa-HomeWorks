def count_word_lengths(sentence):
    words = sentence.split()
    
    for word in words:
        # ბეჭდავს სიტყვას და მის სიგრძეს
        print(f"სიტყვა '{word}' შედგება {len(word)} სიმბოლოსგან.")

# მაგალითი:
count_word_lengths("მე ვსწავლობ კოდინგს")
# გამონატანი:
# სიტყვა 'მე' შედგება 2 სიმბოლოსგან.
# სიტყვა 'ვსწავლობ' შედგება 8 სიმბოლოსგან.
# სიტყვა 'კოდინგს' შედგება 7 სიმბოლოსგან.