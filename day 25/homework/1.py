# str() - მონაცემი გადაჰყავს ტექსტურ (string) ფორმატში
# float() - მონაცემი გადაჰყავს ათწილადის (floating-point number) ფორმატში
# range() - აგენერირებს რიცხვების მიმდევრობას (ხშირად გამოიყენება for ციკლში)
# sum() - ითვლის სიაში ან სხვა კოლექციაში არსებული რიცხვების ჯამს
# max() - აბრუნებს ყველაზე დიდ მნიშვნელობას
