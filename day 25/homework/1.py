# len() - აბრუნებს ელემენტების რაოდენობას სიაში, სტრინგში ან სხვა კოლექციაში
# type() - არკვევს, თუ რა ტიპის არის მოცემული ცვლადი ან მონაცემი (მაგ. int, str, list)
