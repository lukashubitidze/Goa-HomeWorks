A = {1, 2, 3, 4, 5}

# ელემენტების რაოდენობა
print("ელემენტების რაოდენობა:", len(A))  # გამონატანი: 5

# არის თუ არა 3 სეტის ელემენტი
print("არის თუ არა 3 სეტში:", 3 in A)  # გამონატანი: True

# რიცხვი 6-ის დამატება
A.add(6)
print("6-ის დამატების შემდეგ:", A)  # გამონატანი: {1, 2, 3, 4, 5, 6}

# რიცხვი 2-ის ამოღება
A.remove(2)
print("2-ის ამოღების შემდეგ:", A)  # გამონატანი: {1, 3, 4, 5, 6}