vowels = ['бғҗ', 'бғ”', 'бғҳ', 'бғқ', 'бғЈ']
correct_count = 0

for i in range(5):
    word = input(f"бғЁбғ”бғҳбғ§бғ•бғҗбғңбғ”бғ— бғЎбғҳбғўбғ§бғ•бғҗ ({i+1}/5): ")
    
    if word[0] not in vowels and word[-1] not in vowels:
        correct_count += 1 # бғ—бғЈ бғЎбғ¬бғқбғ бғҳбғҗ, бғӣбғ—бғ•бғҡбғ”бғҡбғЎ бғ•бғ–бғ бғ“бғҳбғ— 1-бғҳбғ—

print(f"\nбғ—бғҘбғ•бғ”бғң бғЎбғЈбғҡ бғЁбғ”бғҳбғ§бғ•бғҗбғңбғ”бғ— {correct_count} бғЎбғ¬бғқбғ бғҳ бғЎбғҳбғўбғ§бғ•бғҗ.")