vowels = ['бғҗ', 'бғ”', 'бғҳ', 'бғқ', 'бғЈ']

while True:
    word = input("бғЁбғ”бғҳбғ§бғ•бғҗбғңбғ”бғ— бғЎбғҳбғўбғ§бғ•бғҗ: ")
    
    # бғ•бғҳбғҰбғ”бғ‘бғ— бғһбғҳбғ бғ•бғ”бғҡ бғ“бғҗ бғ‘бғқбғҡбғқ бғҗбғЎбғқбғЎ
    first_letter = word[0]
    last_letter = word[-1]
    
    # бғ•бғҗбғӣбғқбғ¬бғӣбғ”бғ‘бғ—, бғ бғқбғӣ бғҗбғ бғӘбғ”бғ бғ—бғҳ бғҗбғ  бғҳбғ§бғқбғЎ бғ®бғӣбғқбғ•бғҗбғңбғ—бғҗ бғЎбғҳбғҗбғЁбғҳ
    if first_letter not in vowels and last_letter not in vowels:
        print(f"бғҷбғҗбғ бғ’бғҳбғҗ! бғЎбғҳбғўбғ§бғ•бғҗ '{word}' бғЎбғ¬бғқбғ бғҳбғҗ!")
        break
    else:
        print("бғҗбғ бғҗбғЎбғ¬бғқбғ бғҳбғҗ, бғЎбғӘбғҗбғ“бғ”бғ— бғ—бғҗбғ•бғҳбғ“бғҗбғң (бғһбғҳбғ бғ•бғ”бғҡбғҳ бғҗбғң бғ‘бғқбғҡбғқ бғҗбғЎбғқ бғ®бғӣбғқбғ•бғҗбғңбғҳбғҗ).")